
var number = 11;

var outputText = '';



switch(number) {
    case 0 :
        outputText = 'zero';
        break;
    case 1 :
        outputText = 'one';
        break;
    case 2 :
        outputText = 'two';
        break;
    case 3 :
        outputText = 'three';
        break;
    case 4 :
        outputText = 'four';
        break;
    case 5 :
        outputText = 'five';
        break;
    case 6 :
        outputText = 'six';
        break;
    case 7 :
        outputText = 'seven';
        break;
    case 8 :
        outputText = 'eight';
        break;
    case 9 :
        outputText = 'nine';
        break;
    default :
        outputText = 'Not a digit';
        break;
}

console.log(outputText);
